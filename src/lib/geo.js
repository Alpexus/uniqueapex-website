/** "H2V 2K3" | "h2v2k3" → "H2V" (or null if it doesn't look like a postal code) */
export function fsaOf(postal) {
  const m = String(postal || "").trim().toUpperCase().match(/^[A-Z]\d[A-Z]/);
  return m ? m[0] : null;
}

/** postal code → [lat, lng] via FSA centroid, or null if unknown */
export function coordsForPostal(postal) {
  const f = fsaOf(postal);
  return (f && FSA_CENTROIDS[f]) || null;
}

/** great-circle distance in km */
export function haversineKm(lat1, lng1, lat2, lng2) {
  const R = 6371, toR = Math.PI / 180;
  const dLat = (lat2 - lat1) * toR, dLng = (lng2 - lng1) * toR;
  const a = Math.sin(dLat / 2) ** 2 +
    Math.cos(lat1 * toR) * Math.cos(lat2 * toR) * Math.sin(dLng / 2) ** 2;
  return 2 * R * Math.asin(Math.sqrt(a));
}
